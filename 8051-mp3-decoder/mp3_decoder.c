/* Register read to STA013 */
unsigned char i2c_decoder_read(uint8_t addr)
{
    uint8_t rx_data = 0;
    i2c_start();
    send_byte(MP3_DECODER_ADDR);
    send_byte(addr);
    i2c_stop();
    i2c_start();
    send_byte(MP3_DECODER_ADDR | 0x1);

    rx_data = read_byte();

    /* Receiving ACK */
    SCL_Low;
    SDA_High; /* To accept input */
    delay_us(bitDelay);

    /* Wait until ACK is received */
    while(SDA);
    SCL_High;

    /* Toggle SCL to low */
    delay_us(bitDelay/2);
    SCL_Low;
    delay_us(bitDelay/2);

    i2c_stop();

    return rx_data;
}


/* Register write to STA013 */
void i2c_decoder_write(uint8_t addr, uint8_t wr_byte)
{
    i2c_start();
    send_byte(MP3_DECODER_ADDR);
    send_byte(addr);
    send_byte(wr_byte);
    i2c_stop();
}


/* Writing to internal STA013 Start register */
void i2c_decoder_start()
{
    i2c_decoder_write(STA013_START_ADDR, 1);
}


/* Writing to internal STA013 Play register */
void i2c_decoder_play()
{
    i2c_decoder_write(STA013_PLAY_ADDR, 1);
}


/* Sending all initialization data */
void decoder_init()
{
    uint8_t init_read = 0;

    /* Read from address 0x01 to check STA013 */
    init_read = i2c_decoder_read(INIT_ADDR);

    if(init_read != 0xAC) /*Indicate error */
        printf_tiny("ERROR: STA013 not initialized\n\r");

    /* Send 2007 writes from config */
    for(uint32_t i = 0; i < CONFIG_ARR_SIZE; i+=2)
    {
        i2c_decoder_write(STA013_configData[i], STA013_configData[i+1]);

        if(STA013_configData[i] == SOFT_REBOOT_ADDR)
            delay_us(1000000);
    }

    for(uint8_t i = 0; i < PLL_ARR_SIZE; i+=2)
    {
        i2c_decoder_write(PLL_init[i], PLL_init[i+1]);
    }
}
